#include<stdio.h>
void main()
{
    int x;
    printf("ENTER THE NUMBER BETWEEN 0 TO 6\n");
    scanf("%d",&x);
    if(x==0)
    printf("IT'S SUNDAY..!!\n");
    else if(x==1)
    printf("IT'S MONDAY..!!\n");
    else if(x==2)
    printf("IT'S TUESDAY..!!\n");
    else if(x==3)
    printf("IT'S WEDNESDAY..!!\n");
    else if(x==4)
    printf("IT'S THURSDAY..!!\n");
    else if(x==5)
    printf("IT'S FRIDAY..!!\n");
    else
    printf("IT'S SATURDAY..!!\n");

}
